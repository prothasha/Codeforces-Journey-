#include<bits/stdc++.h>
using namespace std;
int main() {
    int n; cin>>n;
    while(n--) {
        string s; cin>>s;
        string w="314159265358979323846264338327";
        int c=0;
        for(int i=0; i<s.size(); i++) {
            if(s[i]==w[i]) c++;
            else break;
        }
        cout<<c<<endl;
    }
    return 0;
}
